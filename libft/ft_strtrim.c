#include "libft.h"

char	*ft_strtrim(char const *s)
{
	size_t	i;
	size_t	j;
	size_t	k;

	if (!s)
		return (NULL);
	i = 0;
	while (ft_isspace(s[i]) == 1)
		i++;
	j = 0;
	k = 0;
	while (s[k])
	{
		if (ft_isspace(s[k]) == 0)
		{
			k++;
			j = k;
		}
		else
			k++;
	}
	if (j == 0)
		return (ft_strdup(""));
	return (ft_strsub(s, i, j - i));
}
